    public void refreshList() {
        
        Class_Usuarios usuarios = new Class_Usuarios();
        DefaultListModel lista = usuarios.carregaLista("");
        listUsuarios.setModel(lista);
    }
    
    public void refreshCampos() {
        
        Class_Usuarios usuarios = new Class_Usuarios();
        usuarios.carregaUsuario((String) listUsuarios.getSelectedValue());
        txtNomeUsuario.setText(usuarios.getNome());
        txtSenhaUsuario.setText(usuarios.getSenha());
        txtTipoUsuario.setText(usuarios.getTipo());
        id_usuario = usuarios.getIdUsuario();
    }
    
    public void alteraCadastroUsuario() {
        Class_Usuarios usuarios = new Class_Usuarios();
        if (usuarios.verificaCadastroExistente(txtNomeEditar.getText()) && !nome_usuario.equals(txtNomeEditar.getText())) {
            JOptionPane.showMessageDialog(null, "Este usuário já existe!", "Atenção", JOptionPane.WARNING_MESSAGE);
            txtNomeEditar.grabFocus();
            txtNomeEditar.selectAll();
        } else {
            Class_Caixa caixa = new Class_Caixa();
            if (!txtNomeEditar.getText().equals(nome_usuario) && caixa.verificaResponsavelCaixaAberto(id_usuario) == true) {
                JOptionPane.showMessageDialog(null, "O nome do usuário não pode ser mudado pois ele possui um caixa aberto!\nfeche o seu caixa para depois mudar o seu nome!", "Atenção", JOptionPane.WARNING_MESSAGE);
            } else {
                usuarios.alteraUsuario(txtNomeEditar.getText(), txtSenhaEditar.getText(), id_usuario);
                refreshList();
                JOptionPane.showMessageDialog(null, "Usuário alterado com sucesso!", "Atenção", JOptionPane.INFORMATION_MESSAGE);

                Alterar_Usuario.dispose();
            }
        }
    }

