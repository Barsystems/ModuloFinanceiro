    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        
        Object usuario = listUsuarios.getSelectedValue();
        if (usuario == null) {
            JOptionPane.showMessageDialog(null, "Selecione o usuário para excluir!", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else {
            if (usuario.toString().equals("Administrador")) {
                JOptionPane.showMessageDialog(null, "Este é um usuário padrão e não poderá ser excluído!", "Atenção", JOptionPane.WARNING_MESSAGE);
            } else {
                Class_Caixa caixa = new Class_Caixa();
                if (caixa.verificaResponsavelCaixaAberto(id_usuario) == true) {
                    JOptionPane.showMessageDialog(null, "Este usuário não poderá ser excluído pois ele possui um caixa aberto!\nFeche o caixa para poder excluí-lo!", "Atenção", JOptionPane.WARNING_MESSAGE);
                } else {
                    if (JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", "Atenção", JOptionPane.YES_NO_OPTION) == 0) {
                        Class_Usuarios usuarios = new Class_Usuarios();
                        usuarios.excluiUsuario(id_usuario);

                        refreshList();
                    }
                }
            }
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

