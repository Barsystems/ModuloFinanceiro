    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        
        Object usuario = listUsuarios.getSelectedValue();
        if (usuario == null) {
            JOptionPane.showMessageDialog(null, "Selecione o usuário para alterar!", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else {
            if (usuario.toString().equals("Administrador")) {
                JOptionPane.showMessageDialog(null, "Este é um usuário padrão e não poderá ser alterado!", "Atenção", JOptionPane.WARNING_MESSAGE);
            } else {
                Class_Usuarios usuarios = new Class_Usuarios();
                usuarios.carregaUsuario(txtNomeUsuario.getText());
                txtNomeEditar.setText(usuarios.getNome());
                nome_usuario = txtNomeEditar.getText();
                txtSenhaEditar.setEnabled(false);
                txtRepetirSenhaEditar.setEnabled(false);
                txtSenhaEditar.setText(usuarios.getSenha());
                txtRepetirSenhaEditar.setText("");
                checkAlterarSenha.setEnabled(true);
                checkAlterarSenha.setSelected(false);
                lblTipoEditarUsuario.setText(usuarios.getTipo());
                
                Alterar_Usuario.setBounds(0, 0, 460, 370);
                Alterar_Usuario.setLocationRelativeTo(null);
                Alterar_Usuario.setVisible(true);
            }
        }
        
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnSalvarCadastro1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarCadastro1ActionPerformed
        
        if (txtNomeEditar.getText().isEmpty() || txtSenhaEditar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Não pode haver campos vazios!", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else {
            if (checkAlterarSenha.isSelected()) {
                if (!txtSenhaEditar.getText().equals(txtRepetirSenhaEditar.getText())) {
                    JOptionPane.showMessageDialog(null, "As senhas não coincidem! Digite a senha novamente!", "Atenção", JOptionPane.WARNING_MESSAGE);
                    txtRepetirSenhaEditar.grabFocus();
                    txtRepetirSenhaEditar.selectAll();
                } else {
                    alteraCadastroUsuario();
                }
            } else {
                alteraCadastroUsuario();
            }
        }
        
    }//GEN-LAST:event_btnSalvarCadastro1ActionPerformed

